    if show_banner_before:
        show_banner(use_lolcat=True)
        time.sleep(0.25)

    info = generate_info_full()
    for key, value in info.items():
        prefix = timestamp_prefix_colored()
        print(f"{prefix}{BLUE}{key}: {value}{RESET}")
        time.sleep(delay_between_lines)

